        public override string[] GetRolesForUser(string username)
        {
            using (var dbObj = new EmployeeDBEntities())
            {
                var result = (from user in dbObj.Users
                              join role in dbObj.Roles on user.roleid equals role.id
                              where user.username == username
                              select role.role).ToArray();

                return result;
            }
